			<h1>Laços de repetição:</h1>
		<p>Os laços de repetição servem para continuar fazendo a mesma ação repetidas vezes, o que ajuda a economizar linhas escritas.</p>
			<div class="repeticao">
				<i>While</i>
					<p>O while vai continuar repetindo todo o código que estiver dentro dele, até que a condição nele deixe de ser satisfeita. A variável que forma a condição dele deve ser inicializada fora do while, antes de iniciar. E a variável que auxilia a condição deve ser acrescentada dentro do while.</p>
					<p><b>Exemplo</b></p>
					<img src="" alt="">
					<br>
				<i>For</i>
					<p>O for assim como o while repete todo o conteúdo dentro dele, até que a condição dele deixe de ser satisfeita. A diferença é que o For é usado para quando você sabe quantas vezes quer repetir aquele código, enquanto While é para situações que você não sabe até quando vai continuar repetindo aquilo. Sem contar que, todo o conteúdo da condição do For é feito em apenas uma linha. VOCÊ PODE ATÉ DECLARAR A VARIÁVEL QUE ACOMPANHARÁ A CONDIÇÃO NA MESMA LINHA </p>
					<p><b>Exemplo</b></p>
					<img src="" alt="">
					<br>
				<i>Do_While</i>
					<p>O Do/While diferentemente dos outros dois, vai realizar o conteúdo dentro do Do, pelo meno uma vez. Depois é realizado o teste de condição e caso o resultado seja falso, ele não fará aquilo novamente. Ele segue o mesmo principio que o While para configurar a variável que cuidará da condição. Mas, ao invés do While abrir o laço, quem abre é o Do e ele é fechado pelo While e sua condição.</p>
